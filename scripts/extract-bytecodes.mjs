import fs from "node:fs";
import path from "node:path";

function getArg(name) {
  const idx = process.argv.indexOf(name);
  if (idx === -1) return undefined;
  return process.argv[idx + 1];
}

const artifactsRoot =
  getArg("--artifactsRoot") ??
  path.resolve(process.cwd(), "node_modules/@evvm/testnet-contracts/out");

const outFile =
  getArg("--outFile") ??
  path.resolve(process.cwd(), "src/lib/contracts/bytecodes.ts");

const mapping = {
  STAKING_BYTECODE: "Staking.sol/Staking.json",
  EVVM_CORE_BYTECODE: "Core.sol/Core.json",
  CORE_HASH_UTILS_BYTECODE: "CoreHashUtils.sol/CoreHashUtils.json",
  NAME_SERVICE_BYTECODE: "NameService.sol/NameService.json",
  ESTIMATOR_BYTECODE: "Estimator.sol/Estimator.json",
  TREASURY_BYTECODE: "Treasury.sol/Treasury.json",
  P2P_SWAP_BYTECODE: "P2PSwap.sol/P2PSwap.json",
};

function readBytecodeObject(artifactPath) {
  const raw = fs.readFileSync(artifactPath, "utf8");
  const json = JSON.parse(raw);
  const obj = json?.bytecode?.object;
  if (typeof obj !== "string" || obj.length === 0) {
    throw new Error(`Missing bytecode.object in ${artifactPath}`);
  }
  return obj.startsWith("0x") ? obj : `0x${obj}`;
}

const bytecodes = {};
let coreLinkReferences = null;
for (const [exportName, rel] of Object.entries(mapping)) {
  const artifactPath = path.resolve(artifactsRoot, rel);
  if (!fs.existsSync(artifactPath)) {
    throw new Error(`Artifact not found: ${artifactPath}`);
  }
  const raw = fs.readFileSync(artifactPath, "utf8");
  const json = JSON.parse(raw);
  const obj = json?.bytecode?.object;
  if (typeof obj !== "string" || obj.length === 0) {
    throw new Error(`Missing bytecode.object in ${artifactPath}`);
  }
  bytecodes[exportName] = obj.startsWith("0x") ? obj : `0x${obj}`;

  if (exportName === "EVVM_CORE_BYTECODE") {
    coreLinkReferences = json?.bytecode?.linkReferences ?? null;
  }
}

const header = `// EVVM Contract Bytecodes for Base Sepolia deployment
// Generated by scripts/extract-bytecodes.mjs
// Source artifacts root: ${artifactsRoot}
//
// NOTE: These are *creation bytecodes* (Foundry artifact bytecode.object), suitable for deployContract().
`;

const body = `${Object.entries(bytecodes)
  .map(([name, value]) => `export const ${name} = ${JSON.stringify(value)} as \`0x\${string}\`;`)
  .join("\n")}

export const EVVM_CORE_LINK_REFERENCES = ${JSON.stringify(coreLinkReferences, null, 2)} as const;

export const hasBytecodes = (): boolean => {
  return !!(
    STAKING_BYTECODE &&
    EVVM_CORE_BYTECODE &&
    CORE_HASH_UTILS_BYTECODE &&
    NAME_SERVICE_BYTECODE &&
    ESTIMATOR_BYTECODE &&
    TREASURY_BYTECODE &&
    P2P_SWAP_BYTECODE
  );
};
`;

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.mkdirSync(path.dirname(path.resolve(process.cwd(), "scripts")), { recursive: true });
fs.writeFileSync(outFile, `${header}\n${body}`, "utf8");

// Print a short summary for humans.
for (const [name, value] of Object.entries(bytecodes)) {
  console.log(`${name}: ${value.length} chars`);
}
console.log(`Wrote ${outFile}`);

